
public class Info {
    int num;
    String name;
    String email;
    void setNum(int num) {
        this.num = num;
    }
    int getNum() {
        return num;
    }
    void setName(String name) {
        this.name = name;
    }
    String getName() {
        return name;
    }
    void setEmail(String email) {
        this.email = email;
    }
    String getEmail() {
        return email;
    }
}
